//统计棋盘上棋子数目
int countChess(int **_board,int row,int column)
{
	int count = 0;
	for(int i = 0;i < row;i++)
		for(int j = 0;j < column;j++)
			if(_board[i][j] > 0)
				count++;
	return count;
}

